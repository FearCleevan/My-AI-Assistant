def get_seed_urls(topic: str) -> list:
    normalized = topic.lower().strip()

    # Direct match
    if normalized in TOPIC_SEEDS:
        return TOPIC_SEEDS[normalized]

    # Alias match
    if normalized in _ALIASES:
        key = _ALIASES[normalized]
        if key in TOPIC_SEEDS:
            return TOPIC_SEEDS[key]

    # Partial alias match (user typed a substring of an alias)
    for alias, key in _ALIASES.items():
        if alias in normalized:
            if key in TOPIC_SEEDS:
                return TOPIC_SEEDS[key]

    # Partial key match (user typed a substring of a key)
    for key in TOPIC_SEEDS:
        if key in normalized or normalized in key:
            return TOPIC_SEEDS[key]

    return []


def list_all_topics() -> list[str]:
    """Return all supported topic names (for display in UI)."""
    return sorted(TOPIC_SEEDS.keys())
